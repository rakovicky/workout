<h1>Čo je to Street workout?</h1>
<p>
Mohli ste sa s tým stretnúť v súvislosti s rôznymi označeniami. Street workout, gheto workout, outdoor workout, prípadne kalistenika. Pri tejto športovej aktivite sa nepoužívajú moderné stroje a nepotrebujete k tomu žiadne drahé tréningové náradie. Postačí nájsť si vhodné miesto niekde v parku alebo na ulici, kde je k dispozícii aspoň nejaká tyč v podobe hrazdy alebo niekoľko jednoduchých preliezok. Potom je vašim jediným tréningovým nástrojom vaše telo a s trochou fantázie môžete absolvovať veľmi účinné cvičenie.
</p>
<img src="img/o_nas.jpg">
<p>
Steet workout je vo svojej základnej podstate kombináciou funkčného tréningu a kalisteniky (cvičenie s vlastnou váhou). Je dobre známe, že moderná forma tejto činnosti vznikla v New Yorku, kde jedinci z chudobných štvrtí nemali zvyšné peniaze na návštevy posilňovní a hľadali rýchle a účinné alternatívy cvičenia.
</p>
<p>
Ďalším dôvodom bola určite aj voľnosť, keďže mohli cvičiť kedy chceli a prakticky aj kde chceli. V mestských parkoch začali trénovať s vlastnou hmotnosťou a tak vznikol tento novodobý šport, ktorý v posledných rokoch rýchlo naberá na popularite. Veľmi obľúbený je tiež v krajinách bývalého Sovietskeho zväzu, no aj u nás stále rastie počet ľudí, ktorí sa tomu venujú pravidelne.
</p>
